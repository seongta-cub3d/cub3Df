#include "libft.h"

int	ft_isalnum(int c)
{
	int	ans;

	if (('a' <= c && c <= 'z') || ('A' <= c && c <= 'Z'))
		ans = 1;
	else if ('0' <= c && c <= '9')
		ans = 1;
	else
		ans = 0;
	return (ans);
}
